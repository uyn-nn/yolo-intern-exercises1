//Viết hàm kiểm tra tính đồng nhất chẵn lẻ của 2 số
let a = 2, b = 3;
if ( a%2 === 0 && b%2 === 0 ) {
    console.log(' Hai số trên đồng nhất chẵn');
} else if ( a%2 != 0 && b%2 != 0 ) {
    console.log('Hai số trên đồng nhất lẻ');
} else {
    console.log('Hai số trên không đồng nhất chẵn lẻ');
}


